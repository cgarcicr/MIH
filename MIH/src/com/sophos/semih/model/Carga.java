package com.sophos.semih.model;

import java.math.BigDecimal;


public class Carga implements java.io.Serializable {

	private static final long serialVersionUID = -7255700476734794968L;
	private TMihControlcarga carga;
	private BigDecimal estadoCarga;
	
	public Carga() {
		
	}

	public Carga(TMihControlcarga carga, BigDecimal estadoCarga) {		
		this.carga = carga;
		this.estadoCarga = estadoCarga;
	}
	
//	Getters y Setters

	
	public TMihControlcarga getCarga() {
		return carga;
	}

	public void setCarga(TMihControlcarga carga) {
		this.carga = carga;
	}

	public BigDecimal getEstadoCarga() {
		return estadoCarga;
	}

	public void setEstadoCarga(BigDecimal estadoCarga) {
		this.estadoCarga = estadoCarga;
	}
	
}
